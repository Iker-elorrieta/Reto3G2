package modelo;

public class Cliente {
	private String dniUs;
	private String nombreUs;
	private String apellidosUs;
	private String contrasenaUs;
	private char sexoUs;
	public String getDniUs() {
		return dniUs;
	}
	public void setDniUs(String dniUs) {
		this.dniUs = dniUs;
	}
	public String getNombreUs() {
		return nombreUs;
	}
	public void setNombreUs(String nombreUs) {
		this.nombreUs = nombreUs;
	}
	public String getApellidosUs() {
		return apellidosUs;
	}
	public void setApellidosUs(String apellidosUs) {
		this.apellidosUs = apellidosUs;
	}
	public String getContrasenaUs() {
		return contrasenaUs;
	}
	public void setContrasenaUs(String contrasenaUs) {
		this.contrasenaUs = contrasenaUs;
	}
	public char getSexoUs() {
		return sexoUs;
	}
	public void setSexoUs(char sexoUs) {
		this.sexoUs = sexoUs;
	}
	
}
